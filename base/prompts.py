        f"  → 应抽：智能商城（product，专有名词）；不抽：建设方案（动名词+泛化后缀\"方案\"）\n"
        f"- 查询「上周签订的采购合同金额是多少」\n"
        f"  → 应抽：采购合同（topic，动名词+具体名词\"合同\"属法律文书）；"
        f"不抽：签订（纯动词）、上周（纯日期）、金额（泛化名词，此处是定语修饰非被询问对象）\n"
